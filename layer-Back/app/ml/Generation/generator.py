        for i, ch in enumerate(chunks):
            logger.debug(f"Chunk {i+1}: [{ch.get('chunk_type')}] {ch.get('text', '')[:400]}...")

        context = build_context(chunks)
        prompt = f"–í–æ—Ç –º–∞—Ç–µ—Ä–∏–∞–ª—ã —É–≥–æ–ª–æ–≤–Ω–æ–≥–æ –¥–µ–ª–∞:\n\n{context}\n\n–°–æ—Å—Ç–∞–≤—å –ø–æ–ª–Ω–æ—Ü–µ–Ω–Ω–æ–µ –ø–æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ –æ –∫–≤–∞–ª–∏—Ñ–∏–∫–∞—Ü–∏–∏ —É–≥–æ–ª–æ–≤–Ω–æ–≥–æ –ø—Ä–∞–≤–æ–Ω–∞—Ä—É—à–µ–Ω–∏—è –Ω–∞ —Ä—É—Å—Å–∫–æ–º."

